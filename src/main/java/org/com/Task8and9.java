package org.com;

public class Task8and9 {
    public static void main(String[] args) {
        // 8. Display the numbers from 1 to 10 using a while loop
        System.out.println("Display the numbers from 1 to 10");
        int i = 1;
        while(i<=10){
            System.out.println(i);
            i++;
        }

        System.out.println("\n");

        // 9. Display the numbers from 10 to 1 using a while loop
        System.out.println("Display the numbers from 10 to 1");
        int n = 10;
        while(n>=1) {
            System.out.println(n);
            n--;
        }
    }
}
